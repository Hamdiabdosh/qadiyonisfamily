export const BASE_DICTS: Record<I18nLang, I18nDict> = {
  en: BASE_EN,
  om: BASE_OM,
  am: BASE_AM,
};

export const ALL_TRANSLATION_KEYS = Object.keys(BASE_EN).sort();

export type TranslationKey = keyof typeof BASE_EN;

export function getDefaultTranslation(lang: I18nLang, key: string): string {
  return BASE_DICTS[lang][key] ?? BASE_EN[key] ?? "";
}

export function buildMergedDict(lang: I18nLang, overrides: Record<string, string>): I18nDict {
  const merged = { ...BASE_DICTS[lang] };
  for (const [compoundKey, value] of Object.entries(overrides)) {
    const dot = compoundKey.indexOf(".");
    if (dot === -1) continue;
    const overrideLang = compoundKey.slice(0, dot);
    const key = compoundKey.slice(dot + 1);
    if (overrideLang === lang) merged[key] = value;
  }
  return merged;
}

export type AdminTranslationRow = {
  key: string;
  en: string;
  om: string;
  am: string;
  defaults: Record<I18nLang, string>;
  overridden: Record<I18nLang, boolean>;
};

export function buildAdminTranslationCatalog(
  dbRows: Array<{ key: string; lang: string; value: string }>,
): AdminTranslationRow[] {
  const overrides = new Map<string, string>();
  const extraKeys = new Set<string>();

  for (const row of dbRows) {
    overrides.set(`${row.lang}:${row.key}`, row.value);
    extraKeys.add(row.key);
  }

  const keys = [...new Set([...ALL_TRANSLATION_KEYS, ...extraKeys])].sort();

  return keys.map((key) => {
    const defaults = {
      en: getDefaultTranslation("en", key),
      om: getDefaultTranslation("om", key),
      am: getDefaultTranslation("am", key),
    };

    const read = (lang: I18nLang) => overrides.get(`${lang}:${key}`) ?? defaults[lang];

    return {
      key,
      en: read("en"),
      om: read("om"),
      am: read("am"),
      defaults,
      overridden: {
        en: overrides.has(`en:${key}`),
        om: overrides.has(`om:${key}`),
        am: overrides.has(`am:${key}`),
      },
    };
  });
}
